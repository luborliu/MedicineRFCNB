package bo.cnb;

public class Pair {
	
	private String word;
	private int frequency;
	
	public Pair() {
		this.word = "";
		this.frequency = 0;
		
	}
	
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public int getFrequency() {
		return frequency;
	}
	public void setFrequency(int frequency) {
		this.frequency = frequency;
	}
	
	

}
